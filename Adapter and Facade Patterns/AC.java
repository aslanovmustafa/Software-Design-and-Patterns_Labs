
public class AC {
	
	public AC() {}
	
	
	public void on() {
		System.out.println("The AirConditioner is on");
	}
	
	public void off() {
		System.out.println("The AirConditioner is off");
	}

	public void temp(int temp) {
		System.out.println("Setting temperature to " + temp + " degrees.");
	}
}
